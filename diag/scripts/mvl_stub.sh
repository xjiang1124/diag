#!/bin/bash

./xo3dcpld -mdiowr 0x16 0x3 0x06
sleep 1
./xo3dcpld -mdiowr 0x12 0x3 0x18
sleep 1
./xo3dcpld -mdiowr 0x10 0x3 0x18
sleep 1

p0=$(./xo3dcpld -mdiord 0x11 0x3)
echo $p0 
if [[ $(( $p0 & 0xff )) != 0 ]]; then
    echo "MVL STUB TEST FAILED"
    exit 1
fi

./xo3dcpld -mdiowr 0x16 0x3 0x0
./xo3dcpld -mdiowr 0x12 0x3 0x0
./xo3dcpld -mdiowr 0x10 0x3 0x0

echo "MVL STUB TEST PASSED"
   
